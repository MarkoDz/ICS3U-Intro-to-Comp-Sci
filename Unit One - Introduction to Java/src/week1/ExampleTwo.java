package week1;

/*
* Escape Sequences
*/

public class ExampleTwo {
    public static void main(String[] args) {
        System.out.println("This is very \"exciting\" code"); // "Quotes" identify the beggining and end of a string literal - text you want to include in the code.
                                                                // backslash \ identifies an escape sequence, it creates a special character.

        System.out.println("This is an error");              // We can only escape certain chraracters (AP escape sequences are \n, \"", \\).

        System.out.println("This\nis\na\nvalid\nescape\nsequence.\n");

    }

    
}


